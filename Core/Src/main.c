  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_SPI2_Init();
  MX_USB_DEVICE_Init();
  /* USER CODE BEGIN 2 */

  PGA_set_gain(&hspi2, 1);

  /* USER CODE END 2 */

